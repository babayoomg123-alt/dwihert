import math 

# program menghitung volume tabung dan balok
# tabungt 
jari_jari_tabung = 5
tinggi_tabung = 10 
volume_tabung = math.pi * (jari_jari_tabung ** 2) * tinggi_tabung

# balok 
panjang_balok = 10
lebar_balok = 5
tinggi_balok = 4
volume_balok = panjang_balok * lebar_balok * tinggi_balok

# balok
panjang_balok = 10
lebar_balok = 5
tinggi_balok = 4
volume_balok = panjang_balok * lebar_balok * tinggi_balok

print(f"volume tabung: {volume_tabung}")
print(f"volume balok: {volume_balok}")